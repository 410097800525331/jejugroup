const resolvedLangData = (() => {
  if (typeof langData !== "undefined") {
    return langData;
  }

  if (typeof globalThis !== "undefined" && globalThis.langData) {
    return globalThis.langData;
  }

  if (typeof module !== "undefined" && module.exports && typeof require === "function") {
    try {
      const loadedLangData = require("./lang_data.js");
      return loadedLangData;
    } catch {
      return null;
    }
  }

  return null;
})();

const buildLangDataCopyMap = () => {
  if (!resolvedLangData?.ko || !resolvedLangData?.en) {
    return {};
  }

  return Object.entries(resolvedLangData.ko).reduce((map, [key, korean]) => {
    const english = resolvedLangData.en[key];
    if (typeof korean === "string" && typeof english === "string") {
      map[korean] = english;
    }
    return map;
  }, {});
};

